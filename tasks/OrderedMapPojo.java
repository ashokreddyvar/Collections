package tasks;



public class OrderedMapPojo {
	
	 private  int personid;
	 private String name;
	public OrderedMapPojo() {
		super();
	}
	public OrderedMapPojo(int personid, String name) {
		super();
		this.personid = personid;
		this.name = name;
	}
	public int getPersonid() {
		return personid;
	}
	public void setPersonid(int personid) {
		this.personid = personid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	 
	
	
}
